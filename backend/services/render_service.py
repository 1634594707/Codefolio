    def _build_key_strengths(self, gitscore: GitScore, user_data: UserData, copy: dict) -> str:
        """Build key strengths section based on GitScore dimensions."""
        strengths = []
        dimensions = gitscore.dimensions
        
        # Identify top 3 dimensions
        sorted_dims = sorted(
            [
                ("impact", dimensions.get("impact", 0.0), 35),
                ("contribution", dimensions.get("contribution", 0.0), 25),
                ("community", dimensions.get("community", 0.0), 20),
                ("tech_breadth", dimensions.get("tech_breadth", 0.0), 15),
                ("documentation", dimensions.get("documentation", 0.0), 5),
            ],
            key=lambda x: x[1] / x[2],  # Sort by percentage of max
            reverse=True,
        )
        
        # Map dimension names to descriptions
        dim_descriptions = {
            "impact": copy.get("project_impact", "Project Impact"),
            "contribution": copy.get("code_contribution", "Code Contribution"),
            "community": copy.get("community_activity", "Community Activity"),
            "tech_breadth": copy.get("tech_breadth", "Tech Breadth"),
            "documentation": copy.get("documentation", "Documentation"),
        }
        
        for dim_key, score, max_score in sorted_dims[:3]:
            percentage = (score / max_score * 100) if max_score > 0 else 0.0
            if percentage >= 50:  # Only include if at least 50% of max
                strengths.append(f"- **{dim_descriptions[dim_key]}**: {score:.1f}/{max_score} ({percentage:.0f}%)")
        
        if not strengths:
            strengths.append(f"- {copy.get('generalist', 'Generalist')} developer with balanced skills")
        
        return "\n".join(strengths)


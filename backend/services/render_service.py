    def generate_pdf_resume(self, markdown_resume: str) -> bytes:
        """Generate a lightweight PDF without requiring extra dependencies."""
        content_lines = self._wrap_markdown_for_pdf(markdown_resume)
        stream_lines: List[str] = ["BT", "/F1 10 Tf", "50 792 Td", "14 TL"]
        first_line = True
        for line in content_lines:
            escaped = self._escape_pdf_text(line)
            if not first_line:
                stream_lines.append("T*")
            stream_lines.append(f"({escaped}) Tj")
            first_line = False
        stream_lines.append("ET")
        stream = "\n".join(stream_lines).encode("latin-1", errors="replace")

        objects = [
            b"1 0 obj << /Type /Catalog /Pages 2 0 R >> endobj",
            b"2 0 obj << /Type /Pages /Kids [3 0 R] /Count 1 >> endobj",
            b"3 0 obj << /Type /Page /Parent 2 0 R /MediaBox [0 0 595 842] /Resources << /Font << /F1 4 0 R >> >> /Contents 5 0 R >> endobj",
            b"4 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica >> endobj",
            f"5 0 obj << /Length {len(stream)} >> stream\n".encode("latin-1") + stream + b"\nendstream endobj",
        ]

        pdf = bytearray(b"%PDF-1.4\n")
        offsets = [0]
        for obj in objects:
            offsets.append(len(pdf))
            pdf.extend(obj)
            pdf.extend(b"\n")

        xref_offset = len(pdf)
        pdf.extend(f"xref\n0 {len(offsets)}\n".encode("latin-1"))
        pdf.extend(b"0000000000 65535 f \n")
        for offset in offsets[1:]:
            pdf.extend(f"{offset:010d} 00000 n \n".encode("latin-1"))
        pdf.extend(
            f"trailer << /Size {len(offsets)} /Root 1 0 R >>\nstartxref\n{xref_offset}\n%%EOF".encode("latin-1")
        )

        return bytes(pdf)

    def _build_metric_cells(
        self,
        values: List[float],
        accent: str,
        muted: str,
        labels: List[str],
        is_dark: bool = True,
    ) -> str:
        cells = []
        bg_color = "rgba(255, 255, 255, 0.04)" if is_dark else "rgba(139, 107, 63, 0.10)"
        for label, value in zip(labels, values):
            cells.append(
                f"<div style='padding:12px 10px;border-radius:18px;background:{bg_color};text-align:center'>"
                f"<div style='font-size:14px;color:{muted}'>{label}</div>"
                f"<div style='margin-top:6px;font-size:26px;font-weight:800;color:{accent}'>{value:.1f}</div>"
                f"</div>"
            )
        return "".join(cells)

    def _wrap_markdown_for_pdf(self, markdown_resume: str) -> List[str]:
        lines: List[str] = []
        for raw_line in markdown_resume.splitlines():
            line = raw_line.strip()
            if not line:
                lines.append("")
                continue
            lines.extend(wrap(line, width=85) or [""])
        return lines

    def _escape_pdf_text(self, text: str) -> str:
        return text.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")

    @staticmethod
    def _normalize_summary_bullets(text: str) -> List[str]:
        """Turn AI or fallback summary into markdown bullet lines."""
        raw = (text or "").strip()
        if not raw:
            return []
        parts = [ln.strip() for ln in raw.splitlines() if ln.strip()]
        out: List[str] = []
        for s in parts:
            if s.startswith("- "):
                out.append(s)
            elif s.startswith("•"):
                out.append("- " + s[1:].strip())
            else:
                out.append("- " + s)
        if out:
            return out
        return ["- " + raw]

    def generate_markdown_resume(
        self,
        user_data: UserData,
        gitscore: GitScore,
        ai_insights: AIInsights,
        language: str = "en",
    ) -> str:
        """Generate CV-style Markdown: contact, bullet highlights, skills, projects, activity, metrics."""
        copy = self.COPY[language]
        lines: List[str] = []

        total_stars = sum(repo.stars for repo in user_data.repositories)
        total_forks = sum(repo.forks for repo in user_data.repositories)
        repo_count = len(user_data.repositories)
        profile_url = f"https://github.com/{user_data.username}"

        display_name = (user_data.name or "").strip() or user_data.username

        lines.append(f"# {display_name}")
        lines.append("")
        lines.append(f"## {copy['contact_heading']}")
        lines.append("")
        lines.append(
            f"- **{copy['github_contact_label']}:** [{user_data.username}]({profile_url})"
        )
        if user_data.bio:
            lines.append(f"- **{copy['bio_bullet_label']}:** {user_data.bio}")
        lines.append("")

        tech_summary = (ai_insights.tech_summary or "").strip()
        summary_lines = self._normalize_summary_bullets(tech_summary)
        if summary_lines:
            lines.append(f"## {copy['summary_heading']}")
            lines.append("")
            lines.extend(summary_lines)
            lines.append("")

        lines.append(f"## {copy['technical_skills']}")
        lines.append("")

        if user_data.languages:
            total_bytes = sum(user_data.languages.values())
            top_langs = sorted(user_data.languages.items(), key=lambda x: x[1], reverse=True)[:5]
            lines.append(f"**{copy['language_distribution']}**")
            lines.append("")
            for lang_name, byte_count in top_langs:
                pct = (byte_count / total_bytes * 100) if total_bytes > 0 else 0.0
                lines.append(f"- **{lang_name}:** {pct:.1f}%")
            lines.append("")

        skills_service = SkillsInferenceService()
        skills_by_level = skills_service.infer_skills(user_data)
        specializations = skills_service.infer_specializations(user_data)

        if specializations:
            lines.append(f"**{copy['specializations']}:** {', '.join(specializations)}")
            lines.append("")

        for level in ["Expert", "Proficient", "Familiar", "Learning"]:
            bucket = skills_by_level.get(level, [])
            if bucket:
                level_label = copy.get(level.lower(), level)
                lines.append(f"- **{level_label}:** {', '.join(bucket)}")
        lines.append("")

        lines.append(f"## {copy['notable_projects']}")
        lines.append("")
        top_repos = sorted(
            user_data.repositories,
            key=lambda repo: (repo.stars, repo.forks),
            reverse=True,
        )[:5]

        if not top_repos:
            lines.append(copy["no_projects"])
            lines.append("")
        else:
            for repo in top_repos:
                lines.append(f"### {repo.name}")
                lines.append("")
                meta_bits: List[str] = []
                if repo.language:
                    meta_bits.append(repo.language)
                if repo.stars > 0:
                    meta_bits.append(f"⭐ {repo.stars}")
                if repo.forks > 0:
                    meta_bits.append(f"🔀 {repo.forks}")
                if meta_bits:
                    lines.append(f"*{' · '.join(meta_bits)}*")
                    lines.append("")
                if repo.description:
                    lines.append(f"- {repo.description}")
                lines.append(f"- **{copy['repo_url_label']}:** {repo.url}")
                lines.append("")

        lines.append(f"## {copy['public_activity_heading']}")
        lines.append("")
        lines.append(f"- **{copy['total_repos']}:** {repo_count}")
        lines.append(f"- **{copy['total_stars']}:** {total_stars}")
        lines.append(f"- **{copy['total_forks']}:** {total_forks}")
        lines.append(f"- **{copy['total_commits']}:** {user_data.contributions.total_commits_last_year}")
        lines.append(f"- **{copy['total_pull_requests']}:** {user_data.contributions.total_prs_last_year}")
        lines.append(f"- **{copy['followers']}:** {user_data.followers}")
        lines.append(f"- **{copy['following']}:** {user_data.following}")
        if user_data.contributions.longest_streak > 0:
            lines.append(
                f"- **{copy['longest_streak']}:** {user_data.contributions.longest_streak} {copy['days']}"
            )
        lines.append("")

        lines.append(f"## {copy['score_reference_heading']}")
        lines.append("")
        lines.append(f"- **{copy['composite_gitscore_label']}:** **{gitscore.total:.1f}** / 100")
        lines.append("")
        lines.append(f"### {copy['score_breakdown']}")
        lines.append("")
        dimension_rows = [
            ("impact", copy["project_impact"], 35),
            ("contribution", copy["code_contribution"], 25),
            ("community", copy["community_activity"], 20),
            ("tech_breadth", copy["tech_breadth"], 15),
            ("documentation", copy["documentation"], 5),
        ]
        for dim_key, label, cap in dimension_rows:
            score = gitscore.dimensions.get(dim_key, 0.0)
            lines.append(f"- **{label}:** {score:.1f} / {cap}")
        lines.append("")

        if ai_insights.style_tags:
            lines.append(f"## {copy['keywords_heading']}")
            lines.append("")
            lines.append(" · ".join(ai_insights.style_tags))
            lines.append("")

        lines.append(f"*{copy['data_attribution']}*")
        lines.append("")

        return "\n".join(lines)

    def _build_profile_overview(self, user_data: UserData, gitscore: GitScore, copy: dict) -> str:
        """Build profile overview section with key stats."""
        lines = []
        
        # Calculate total stars and forks
        total_stars = sum(repo.stars for repo in user_data.repositories)
        total_forks = sum(repo.forks for repo in user_data.repositories)
        repo_count = len(user_data.repositories)
        avg_stars = total_stars / repo_count if repo_count > 0 else 0
        
        # Developer type based on gitscore dimensions
        dimensions = gitscore.dimensions
        max_dim = max(dimensions.items(), key=lambda x: x[1])
        dev_types = {
            "impact": "Open Source Influencer",
            "contribution": "Core Contributor", 
            "community": "Community Builder",
            "tech_breadth": "Full Stack Developer",
            "documentation": "Technical Writer",
        }
        dev_type = dev_types.get(max_dim[0], "Developer")
        
        # Activity level
        commits = user_data.contributions.total_commits_last_year
        if commits > 1000:
            activity = "🔥 Very High"
        elif commits > 500:
            activity = "⚡ High"
        elif commits > 100:
            activity = "📊 Moderate"
        else:
            activity = "🌱 Growing"
        
        lines.append(f"| **{copy['dev_type']}** | **{copy['activity_level']}** | **GitScore** |")
        lines.append(f"|---|---|---|")
        lines.append(f"| {dev_type} | {activity} | {gitscore.total:.1f}/100 |")
        lines.append("")
        lines.append(f"### {copy['repo_stats']}")
        lines.append("")
        lines.append(f"- **{copy['total_repos']}:** {repo_count}")
        lines.append(f"- **{copy['total_stars']}:** {total_stars} ⭐")
        lines.append(f"- **{copy['total_forks']}:** {total_forks} 🔀")
        lines.append(f"- **{copy['avg_stars']}:** {avg_stars:.1f}")
        
        return "\n".join(lines)
    
    def _build_gitscore_visual(self, gitscore: GitScore, copy: dict) -> str:
        """Build visual GitScore representation with progress bars."""
        lines = []
        dimensions = [
            ("impact", copy['project_impact'], 35),
            ("contribution", copy['code_contribution'], 25),
            ("community", copy['community_activity'], 20),
            ("tech_breadth", copy['tech_breadth'], 15),
            ("documentation", copy['documentation'], 5),
        ]
        
        lines.append(f"### {copy['score_breakdown']}")
        lines.append("")
        
        for dim_key, label, max_val in dimensions:
            score = gitscore.dimensions.get(dim_key, 0.0)
            percentage = (score / max_val * 100) if max_val > 0 else 0
            bar_length = int(percentage / 5)  # 20 chars = 100%
            bar = "█" * bar_length + "░" * (20 - bar_length)
            lines.append(f"- **{label}:** `{bar}` {score:.1f}/{max_val}")
        
        return "\n".join(lines)
    
